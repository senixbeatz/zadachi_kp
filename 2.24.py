#Павлов Арсений, задача 2.24 Даны два числа. Найти их сумму, разность, произведение,а также частное от деления первого числа на второе.
a = int(input())
b = int(input())
print('сумма равна', a + b)
print('разность равна', a - b)
print('произведение равно', a * b)
print('частное от деления первого на второе равно', a / b)